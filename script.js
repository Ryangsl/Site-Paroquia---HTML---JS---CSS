// SCROLL REVEAL ANIMATION (Optional Enhancement)
// ===================================
function revealOnScroll() {
    const reveals = document.querySelectorAll('.schedule-card, .event-card, .about-text, .about-image');
    
    reveals.forEach(element => {
        const windowHeight = window.innerHeight;
        const elementTop = element.getBoundingClientRect().top;
        const elementVisible = 150;
        
        if (elementTop < windowHeight - elementVisible) {
            element.style.opacity = '1';
            element.style.transform = 'translateY(0)';
        }
    });
}

// Initialize elements for scroll reveal
document.addEventListener('DOMContentLoaded', function() {
    const reveals = document.querySelectorAll('.schedule-card, .event-card, .about-text, .about-image');
    reveals.forEach(element => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(20px)';
        element.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
    });
});

window.addEventListener('scroll', revealOnScroll);
revealOnScroll(); // Call once on load

